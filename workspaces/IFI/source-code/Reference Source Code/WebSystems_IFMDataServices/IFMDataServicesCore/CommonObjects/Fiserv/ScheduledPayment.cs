using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace IFM.DataServicesCore.CommonObjects.Fiserv
{
    [System.Serializable]
    public class ScheduledPayment : ModelBase
    {
        public int FiservScheduledPaymentId { get; set; } //generated by IFM after Create
        public string ReferenceId { get; set; } //generated by Fiserv (their key)
        public string ReferenceCode { get; set; } //generated by Fiserv; user sees this reference code in the confirmation email
        public decimal PaymentAmount { get; set; } //Create and Update
        public string PaymentScheduleDate { get; set; } //Create and Update
        public string EmailAddress { get; set; } //Create and Update
        public string FundingAccountToken { get; set; } //Create and Update; corresponds to selected walletItem
        public string FundingCategory { get; set; } //Check, Debit, Credit; corresponds to selected walletItem; not set on Create or Update; should come back from Fiserv
        public string FundingAccountLastFourDigits { get; set; } //corresponds to selected walletItem; not set on Create or Update; should come back from Fiserv
        public string FundingAccountType { get; set; } //Personal, Business; corresponds to selected walletItem; for Bank info; not set on Create or Update; should come back from Fiserv
        public string BankAccountType { get; set; } //Checking, Saving; corresponds to selected walletItem; not set on Create or Update; should come back from Fiserv
        public string PolicyNumber { get; set; } //should be set on Create; should not be changed on Update
        public string AccountBillNumber { get; set; }
        public string KeyIdentifier { get; set; } //Fiserv.Session.UserId
        public int FiservWalletItemId { get; set; } //Create and Update; corresponds to selected walletItem
        public string FeeAmount { get; set; } //added 9/25/2024; string instead of double so it can go to database as NULL when not used (property set to emptyString)

    }


